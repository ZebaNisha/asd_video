# create_labeled_dataset.py
"""Create a labeled dataset by combining extracted features with ASD/TD labels.

The script expects:
- `features.csv` generated by `build_feature_dataset.py`.
- The original dataset directory containing training and testing splits with sub‑folders `asd` and `td`.

It produces:
- `labeled_features.csv` – feature rows annotated with `label` (ASD=1, TD=0) and `split` (train/test).
- `labeling_report.csv` – a log of matching outcomes.
- `duplicate_videos.csv` – a report of video IDs that appear in more than one folder (possible data issue).
"""

import logging
import pandas as pd
from pathlib import Path
import sys
import argparse

from path_config import FEATURES_DIR, REPORTS_ROOT, FILTERING_DIR, CHILD_SEQUENCES_DIR

# ---------------------------------------------------------------------------
# Configuration – adjust if your project layout changes
# ---------------------------------------------------------------------------
FEATURES_CSV = FEATURES_DIR / "features.csv"
DATA_ROOT = Path(r"c:/asd_project") / "autism_data_anonymized" / "autism_data_anonymized"
OUTPUT_CSV = FEATURES_DIR / "labeled_features.csv"
REPORT_CSV = REPORTS_ROOT / "labeling_report.csv"
DUPLICATE_CSV = REPORTS_ROOT / "duplicate_videos.csv"

# ---------------------------------------------------------------------------
# Helper: Build an exact video_id → (label, split, dataset_path) map
# ---------------------------------------------------------------------------
def build_label_map_from_selected(selected_csv: Path):
    """Build label lookup directly from selected_videos.csv.

    Keys are unique_video_id values exactly as written in selected_videos.csv,
    so they match what build_feature_dataset.py stored in features.csv.

    Returns:
        unique_map  : dict  unique_video_id -> {label, split, dataset_path}
        video_id_map: dict  video_id        -> list of entries  (for audit)
    """
    if not selected_csv.is_file():
        raise FileNotFoundError(f"selected_videos.csv not found: {selected_csv}")

    df = pd.read_csv(selected_csv)
    required = {"unique_video_id", "video_id", "label", "split", "dataset_path"}
    missing = required - set(df.columns)
    if missing:
        raise ValueError(f"selected_videos.csv is missing columns: {missing}")

    unique_map = {}
    video_id_map = {}

    for _, row in df.iterrows():
        uid  = str(row["unique_video_id"]).strip()
        vid  = str(row["video_id"]).strip()
        # label column in selected_videos.csv is 'asd'/'td' string; convert to int
        raw_label = str(row["label"]).strip().lower()
        if raw_label in ("1", "asd"):
            label_val = 1
        elif raw_label in ("0", "td"):
            label_val = 0
        else:
            label_val = int(raw_label)  # already numeric

        split_val = str(row["split"]).strip().lower()  # 'train' or 'test'
        dpath     = str(row["dataset_path"]).strip()

        if uid in unique_map:
            raise RuntimeError(f"Duplicate unique_video_id in selected_videos.csv: {uid}")

        unique_map[uid] = {
            "label": label_val,
            "split": split_val,
            "dataset_path": dpath,
        }
        video_id_map.setdefault(vid, []).append({
            "unique_video_id": uid,
            "label": label_val,
            "split": split_val,
            "dataset_path": dpath,
        })

    return unique_map, video_id_map


def parse_args():
    import argparse
    parser = argparse.ArgumentParser(description="Create labeled dataset with optional subject filtering")
    parser.add_argument("--subject", type=str, default=None, help="Video ID substring to output only rows for that subject")
    return parser.parse_args()


# ---------------------------------------------------------------------------
def main():
    # Load feature table
    if not FEATURES_CSV.is_file():
        sys.exit(
            f"Features file not found: {FEATURES_CSV}\n"
            "Run filter_child_sequences.py and build_feature_dataset.py first, "
            "or re-run run_pipeline.py after child sequences are generated."
        )
    features_df = pd.read_csv(FEATURES_CSV)
    # Ensure label column is integer type for later assignment
    if "label" in features_df.columns:
        features_df["label"] = pd.to_numeric(features_df["label"], errors='coerce').fillna(-1).astype(int)
    else:
        features_df["label"] = -1
    # Ensure split and dataset_path columns exist for later assignment
    if "split" not in features_df.columns:
        features_df["split"] = ""
    if "dataset_path" not in features_df.columns:
        features_df["dataset_path"] = ""
    if "unique_video_id" not in features_df.columns:
        features_df["unique_video_id"] = ""
    if features_df.empty:
        sys.exit(
            f"Features file is empty: {FEATURES_CSV}\n"
            "No videos passed filtering or feature extraction. "
            "Check outputs/filtering/accepted_videos.txt and outputs/filtering/rejected_videos.csv."
        )
    total_rows = len(features_df)

    # Build mapping from selected_videos.csv (exact unique_video_id keys)
    selected_csv = REPORTS_ROOT / "selected_videos.csv"
    unique_map, video_id_map = build_label_map_from_selected(selected_csv)
    # Duplicate video_id values are expected (same stem in asd/ and td/).
    # Duplicate unique_video_id values would have raised an error above.


    # Reporting containers
    report_rows = []
    matched = 0
    unmatched = 0
    asd_cnt = 0
    td_cnt = 0

    for idx, row in features_df.iterrows():
        # Prefer deterministic mapping via unique_video_id if present
        if "unique_video_id" in features_df.columns and pd.notnull(row["unique_video_id"]):
            unique_vid = str(row["unique_video_id"]).strip()
            if unique_vid in unique_map:
                mapping = unique_map[unique_vid]
                features_df.at[idx, "label"] = mapping["label"]
                features_df.at[idx, "split"] = mapping["split"]
                features_df.at[idx, "dataset_path"] = mapping["dataset_path"]
                features_df.at[idx, "unique_video_id"] = unique_vid
                matched += 1
                if mapping["label"] == 1:
                    asd_cnt += 1
                else:
                    td_cnt += 1
                report_rows.append({"video_id": row["video_id"], "match_status": "matched"})
                continue
        # Fallback to video_id based matching (original behavior)
        video_id = str(row["video_id"]).strip()
        candidates = video_id_map.get(video_id, [])
        if not candidates:
            status = "unmatched"
            unmatched += 1
        else:
            entry = candidates[0]
            unique_vid = entry["unique_video_id"]
            mapping = unique_map[unique_vid]
            features_df.at[idx, "label"] = mapping["label"]
            features_df.at[idx, "split"] = mapping["split"]
            features_df.at[idx, "dataset_path"] = mapping["dataset_path"]
            features_df.at[idx, "unique_video_id"] = unique_vid
            matched += 1
            if mapping["label"] == 1:
                asd_cnt += 1
            else:
                td_cnt += 1
            status = "matched"
        report_rows.append({"video_id": video_id, "match_status": status})

    # Save outputs
    features_df.to_csv(OUTPUT_CSV, index=False)
    pd.DataFrame(report_rows).to_csv(REPORT_CSV, index=False)
    # Duplicate video_id audit (optional)
    duplicate_vid_audit = []
    for vid, rows in video_id_map.items():
        if len(rows) > 1:
            for r in rows:
                duplicate_vid_audit.append({
                    "video_id": vid,
                    "unique_video_id": r["unique_video_id"],
                    "label": r["label"],
                    "split": r["split"],
                    "dataset_path": r["dataset_path"],
                })
    if duplicate_vid_audit:
        pd.DataFrame(duplicate_vid_audit).to_csv(DUPLICATE_CSV, index=False)
    else:
        # Write empty file to keep pipeline expectations
        pd.DataFrame(columns=["video_id","unique_video_id","label","split","dataset_path"]).to_csv(DUPLICATE_CSV, index=False)

    # ---------------------------------------------------------------
    # Diagnostics & audit generation (updated to use unique IDs)
    # ---------------------------------------------------------------
    # 1. Summary stats
    total_features = total_rows
    unique_features_ids = features_df["video_id"].nunique()
    dup_features = features_df["video_id"].value_counts()
    dup_features = dup_features[dup_features > 1]
    duplicate_features_ids = dup_features.index.tolist()

    total_dataset_videos = len(unique_map)
    unique_dataset_ids = len(unique_map)
    duplicate_dataset_ids = []

    # 2. Class distribution after labeling
    class_dist = features_df.groupby(["split", "label"]).size().reset_index(name="count")

    # 3. Example rows per group (first occurrence)
    example_rows = {}
    for split_dir, label_name in [("training_set", "asd"), ("training_set", "td"), ("testing_set", "asd"), ("testing_set", "td")]:
        target_split = "train" if split_dir == "training_set" else "test"
        target_label = 1 if label_name == "asd" else 0
        subset = features_df[(features_df["split"] == target_split) & (features_df["label"] == target_label)]
        example_rows[(split_dir, label_name)] = subset.head(1)

    # 4. Write diagnostics summary
    summary_path = REPORTS_ROOT / "label_mapping_summary.txt"
    with open(summary_path, "w", encoding="utf-8") as f:
        f.write("Label Mapping Diagnostics Summary\n")
        f.write("================================\n\n")
        f.write(f"1. Total rows in features.csv                : {total_features}\n")
        f.write(f"2. Unique video_ids in features.csv          : {unique_features_ids}\n")
        f.write(f"3. Total videos discovered in dataset folders : {total_dataset_videos}\n")
        f.write(f"4. Unique video_ids in dataset folders       : {unique_dataset_ids}\n")
        f.write(f"5. Number of matched rows                    : {matched}\n")
        f.write(f"6. Number of unmatched rows                  : {unmatched}\n")
        f.write(f"7. Duplicate video_ids in features.csv      : {', '.join(map(str, duplicate_features_ids)) if duplicate_features_ids else 'None'}\n")
        f.write(f"8. Duplicate video_ids in dataset folders    : {', '.join(duplicate_dataset_ids) if duplicate_dataset_ids else 'None'}\n")
        f.write("9. Class distribution after labeling (split, label, count):\n")
        for _, row in class_dist.iterrows():
            f.write(f"   {row['split']}, {row['label']}, {row['count']}\n")
        f.write("10. Example rows from each group (video_id, dataset_path, split, label):\n")
        for (sdir, lname), df_ex in example_rows.items():
            if not df_ex.empty:
                r = df_ex.iloc[0]
                f.write(f"   {sdir}/{lname}: {r['video_id']}, {r.get('dataset_path','')}, {r['split']}, {r['label']}\n")
            else:
                f.write(f"   {sdir}/{lname}: No rows found\n")

    # 5. Write detailed label mapping audit (one row per matched video)
    audit_rows = []
    for idx, row in features_df.iterrows():
        if row["label"] == -1:
            continue
        audit_rows.append({
            "video_id": row["video_id"],
            "dataset_path": row["dataset_path"],
            "split": row["split"],
            "label": row["label"],
        })
    audit_df = pd.DataFrame(audit_rows)
    audit_path = REPORTS_ROOT / "label_mapping_audit.csv"
    audit_df.to_csv(audit_path, index=False)

        # ---------------------------------------------------------------
    # Stage Audit Generation
    # ---------------------------------------------------------------
    # Load selected videos metadata
    selected_path = REPORTS_ROOT / "selected_videos.csv"
    selected_df = pd.read_csv(selected_path) if selected_path.is_file() else pd.DataFrame()
    # Load accepted video list
    accepted_path = FILTERING_DIR / "accepted_videos.txt"
    accepted_set = set()
    if accepted_path.is_file():
        with open(accepted_path, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if line:
                    accepted_set.add(line)
    # Determine child sequence existence per video_id
    child_seq_exists = {}
    for vid in selected_df["video_id"]:
        seq_file = CHILD_SEQUENCES_DIR / f"{vid}_child_sequence.csv"
        child_seq_exists[vid] = seq_file.is_file()
    # Feature rows presence by unique_video_id
    feature_set = set(features_df["unique_video_id"]) if "unique_video_id" in features_df.columns else set()
    # Build audit rows
    audit_rows = []
    for _, row in selected_df.iterrows():
        uid = row["unique_video_id"]
        vid = row["video_id"]
        audit_rows.append({
            "unique_video_id": uid,
            "split": row["split"] if "split" in row else "",
            "label": row["label"] if "label" in row else "",
            "selected": True,
            "child_sequence_exists": child_seq_exists.get(vid, False),
            "accepted_by_filter": vid in accepted_set,
            "feature_row_created": uid in feature_set,
        })
    audit_df = pd.DataFrame(audit_rows)
    stage_audit_path = REPORTS_ROOT / "stage_audit.csv"
    audit_df.to_csv(stage_audit_path, index=False)
    logging.info("Stage audit saved to %s", stage_audit_path)

    args = parse_args()

    if not args.subject:
        for sp in ["train", "test"]:
            sp_df = features_df[features_df["split"] == sp]
            if sp_df["label"].nunique() < 2:
                logging.warning(f"{sp.capitalize()} split contains only one class after labeling; proceeding anyway.")

    # Console summary (unchanged)
    print("=== Labeling Summary ===")
    print(f"Total feature rows      : {total_rows}")
    print(f"Successfully matched   : {matched}")
    print(f"Unmatched rows         : {unmatched}")
    print(f"ASD count (label=1)    : {asd_cnt}")
    print(f"TD count (label=0)     : {td_cnt}")
    if args.subject:
        subject_mask = features_df["video_id"].str.contains(args.subject, na=False)
        subject_df = features_df[subject_mask]
        if subject_df.empty:
            print(f"No rows found for subject pattern '{args.subject}'")
        else:
            subject_output = OUTPUT_CSV.parent / f"{args.subject}_labeled_features.csv"
            subject_df.to_csv(subject_output, index=False)
            print(f"Subject-specific output saved to {subject_output}")

    print(f"Report saved to         : {REPORT_CSV}")
    print(f"Duplicate report saved to: {DUPLICATE_CSV}")
    print(f"Audit CSV saved to      : {audit_path}")
    print(f"Diagnostics saved to   : {summary_path}")

if __name__ == "__main__":
    main()
